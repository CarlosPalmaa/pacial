def calcular_promedio(num1, num2, num3):
    suma = num1 + num2 + num3
    promedio = suma / 3
    return promedio

# Ejemplo de uso:
valor1 = 10
valor2 = 20
valor3 = 30

promedio = calcular_promedio(valor1, valor2, valor3)
print("El promedio de", valor1, ",", valor2, "y", valor3, "es:", promedio)
